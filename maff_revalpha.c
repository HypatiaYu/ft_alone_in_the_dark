#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int		main(void)
{
	int i;

	i = 'z';
	while (i >= 'a')
	{
		if (i % 2 == 1)
			ft_putchar(i - 32);
		else if (i % 2 == 0)
			ft_putchar(i);
		i--;
	}
	ft_putchar('\n');
	return (0);
}
